export default function BrandPage() {
  const [brand, setBrand] = useState<Brand | null>(null);
  const [isLoading, setIsLoading] = useState(true);
  const [isGeneratingDna, setIsGeneratingDna] = useState(false);
  const [isGeneratingMatrix, setIsGeneratingMatrix] = useState(false);
  const [isGeneratingPillars, setIsGeneratingPillars] = useState(false);
  const [isGeneratingIdeas, setIsGeneratingIdeas] = useState(false);
  const params = useParams();
  const router = useRouter();
  const id = params.id as string;

  const fetchBrand = async () => {
    if (!id) return;
    try {
      const response = await fetch(`/api/brands/${id}`);
      if (!response.ok) throw new Error("Brand not found");
      const data = await response.json();
      setBrand(data);
    } catch (error) {
      console.error(error);
      router.push("/dashboard");
    } finally {
      setIsLoading(false);
    }
  };

  useEffect(() => {
    setIsLoading(true);
    fetchBrand();
  }, [id]);

  const handleGenerateDna = async () => {
    setIsGeneratingDna(true);
    try {
      await fetch('/api/generate/brand-discovery', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ brandId: id }),
      });
      await fetchBrand();
    } catch (error) { console.error(error); }
    finally { setIsGeneratingDna(false); }
  };

  const handleGenerateMatrix = async () => {
    setIsGeneratingMatrix(true);
    try {
      await fetch('/api/generate/messaging-matrix', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ brandId: id }),
      });
      await fetchBrand();
    } catch (error) { console.error(error); }
    finally { setIsGeneratingMatrix(false); }
  };

  const handleGeneratePillars = async () => {
    setIsGeneratingPillars(true);
    try {
      await fetch('/api/generate/content-pillars', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ brandId: id }),
      });
      await fetchBrand();
    } catch (error) { console.error(error); }
    finally { setIsGeneratingPillars(false); }
  };

  const handleGenerateIdeas = async () => {
    setIsGeneratingIdeas(true);
    try {
      await fetch('/api/generate/post-ideas', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ brandId: id }),
      });
      await fetchBrand();
    } catch (error) { console.error(error); }
    finally { setIsGeneratingIdeas(false); }
  };

  if (isLoading) {
    return <div className="text-center text-slate-400">Loading Brand...</div>;
  }

  if (!brand) {
    return null;
  }

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.5 }}
      className="space-y-8"
    >
      <div className="mb-8">
        <Link href="/dashboard" className="flex items-center gap-2 text-slate-400 hover:text-brand-silver transition-colors">
          <ArrowLeft size={16} />
          Back to Dashboard
        </Link>
      </div>

      <header>
        <h1 className="text-4xl font-bold text-brand-silver">{brand.name}</h1>
        {brand.description && <p className="text-slate-400 mt-2 max-w-2xl">{brand.description}</p>}
      </header>

      <BrandDnaSection brand={brand} onGenerate={handleGenerateDna} isLoading={isGeneratingDna} />
      <MessagingMatrixSection brand={brand} onGenerate={handleGenerateMatrix} isLoading={isGeneratingMatrix} />
      <ContentPillarsSection brand={brand} onGenerate={handleGeneratePillars} isLoading={isGeneratingPillars} />
      <PostIdeasSection brand={brand} onGenerate={handleGenerateIdeas} isLoading={isGeneratingIdeas} />

    </motion.div>
  );
}
import Link from "next/link";

// Helper component for displaying a single DNA item
const DnaItem = ({ label, value }: { label: string; value: string | string[] | null | undefined }) => {
  if (!value) return null;
  const displayValue = Array.isArray(value) ? value.join(", ") : value;
  return (
    <div>
      <h3 className="text-sm font-semibold text-slate-400 uppercase tracking-wider">{label}</h3>
      <p className="text-brand-silver mt-1">{displayValue}</p>
    </div>
  );
};

// Main component for the Brand DNA section
const BrandDnaSection = ({ brand, onGenerate, isLoading }: { brand: Brand; onGenerate: () => void; isLoading: boolean; }) => {
  const hasDna = brand.mission || brand.vision;

  return (
    <Card className="bg-brand-slate/50 border-slate-800">
      <CardHeader>
        <CardTitle className="text-brand-silver">Brand DNA</CardTitle>
        <CardDescription className="text-slate-400">
          The core strategic foundation of your brand, generated by AI.
        </CardDescription>
      </CardHeader>
      <CardContent>
        {hasDna ? (
          <div className="space-y-6">
            <DnaItem label="Mission" value={brand.mission} />
            <DnaItem label="Vision" value={brand.vision} />
            <DnaItem label="Core Values" value={brand.values as string[]} />
            <DnaItem label="Target Audience" value={brand.targetAudience} />
            <DnaItem label="Unique Selling Proposition" value={brand.usp} />
            <DnaItem label="Personality Traits" value={brand.personalityTraits as string[]} />
          </div>
        ) : (
          <div className="text-center py-10">
            <p className="text-slate-400 mb-6">Your Brand DNA hasn't been generated yet.</p>
            <Button onClick={onGenerate} disabled={isLoading} className="bg-brand-blue hover:bg-blue-700">
              <Wand2 className="mr-2 h-4 w-4" />
              {isLoading ? "Generating..." : "Generate Brand DNA"}
            </Button>
          </div>
        )}
      </CardContent>
    </Card>
  );
};

